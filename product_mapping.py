# Helper functions required by the backend
def get_grozi_code(product_name: str) -> str:
    """Get the grozi code for a product name."""
    for code, name in PRODUCT_NAME_MAP.items():
        if name.lower() == product_name.lower():
            return code
    return ""


def get_display_name(grozi_code: str) -> str:
    """Get the display name for a grozi code."""
    return PRODUCT_NAME_MAP.get(grozi_code, grozi_code)


def get_price(grozi_code: str) -> float:
    """Get the price for a grozi code."""
    return PRODUCT_PRICES.get(grozi_code, 0.00)


def get_category(grozi_code: str) -> str:
    """Get the category for a grozi code."""
    return PRODUCT_CATEGORIES.get(grozi_code, "General")


def get_shelf(grozi_code: str) -> str:
    """Get the shelf/aisle for a grozi code."""
    return PRODUCT_SHELVES.get(grozi_code, "Aisle 9")
